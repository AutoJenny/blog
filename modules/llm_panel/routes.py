@bp.route('/panels')
def panels():
    """Render the LLM panels."""
    post_id = request.args.get('post_id')
    substage = request.args.get('substage', 'idea')
    
    if not post_id:
        return "Post ID is required", 400
        
    # Example step configuration - this should come from your database
    step_config = {
        'inputs': {
            'basic_idea': {
                'type': 'textarea',
                'db_field': 'basic_idea',
                'db_table': 'post_development'
            }
        },
        'outputs': {
            'refined_idea': {
                'type': 'textarea',
                'db_field': 'refined_idea',
                'db_table': 'post_development'
            }
        },
        'settings': {
            'llm': {
                'model': 'mistral',
                'task_prompt': 'Refine the basic idea into a more detailed concept.',
                'input_mapping': {
                    'basic_idea': 'basic_idea'
                },
                'parameters': {
                    'temperature': 0.7,
                    'max_tokens': 1000,
                    'top_p': 1.0,
                    'frequency_penalty': 0.0,
                    'presence_penalty': 0.0
                }
            }
        }
    }
    
    # Example input/output values
    input_values = {
        'basic_idea': 'This is a sample basic idea for testing the panel.'
    }
    
    output_values = {
        'refined_idea': 'This is a sample refined idea that would be generated by the LLM.'
    }
        
    return render_template('panel.html', 
        step_config=step_config,
        input_values=input_values,
        output_values=output_values,
        substage=substage,
        post={'id': post_id}
    ) 